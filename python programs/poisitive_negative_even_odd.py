x = int(input("Enter the number:- "))
if x % 2 == 0:
    print("The number is EVEN:- ", x)
else:
    print("The number is ODD:- ", x)

if x > 0:
    print("The number is positive:- ", x)
elif x == 0:
    print("The number is also a positive number:- ", x)
else:
    print("The number is negative number:- ", x)
